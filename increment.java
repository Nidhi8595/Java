public class increment {
    public static void main(String[] args) {
        // increment/ decrement
        int a = 3;
        int b = 10;
        System.out.println("Value of a->" + a);
        System.out.println("Value of a after use of increment operator->" + (++a));
        System.out.println("Value of b->" + b);
        System.out.println("Value of b after use of decrement operator->" + (--b));
    }
}
