public class conditional {
    public static void main(String[] args) {
        // Conditional operators
        int a = 3, b = 4;
        if (a > b) {
            System.out.println("a is greater");
        }
        if (a == b) {
            System.out.println("a and b are equal");
        }
        if (a < b) {
            System.out.println("b is greater");
        }
    }
}
